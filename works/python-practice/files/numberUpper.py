one_number = int(input("Digite o primeiro valor: "))
two_number = int(input("Digite o segundo valor: "))

if one_number == two_number:
  print("Olha parabéns consegui mano...")
elif one_number > two_number:
  print("Meu primeiro numero é maior: ",one_number)
else:
  print("Meu segundo numero é maior: ",two_number)